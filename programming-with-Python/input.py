# The function input() is used to get input from the user

# Inside the input() function, we pass a message to the user as a parameter
name = input("Enter your name here: ")

# Then, we can concatenate the user input with a message
# using the + operator, which is the concatenation operator
# usually used to combine short and simple strings
print("Hello, " + name)

# Let's try it out! But this time with a number
age = input("Enter your age here: ")
print(name + " is " + age + " years old")