# The lists in Python are used to store multiple items in a single variable
names = ["John", "Bob", "Mosh", "Sarah", "Mary"]

# We can access an specific item in the list using its index
print(names[0]) # This will print the first item in the list

# Or if we want to access a part of the list we can use slicing
print(names[0:2]) # This will print the first two items in the list