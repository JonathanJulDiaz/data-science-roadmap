# The strings always start from 0 to the length of the string,
# so the first character is at index 0, which is 'P' in this case
course = 'Python for Beginners'
#         012345...

# Here we are accessing the first character of the string
print(course[0])

# We can also get the last character of the string using -1
# in this case the last character is 's'
print(course[-1])

# We can also get the second last character of the string
print(course[-2])

# So the String index starts from 0 to the length of the string
# and from the last character to -length too