# Python have many useful functions that we can use on strings
course = 'Python for Beginners'

# For example, len() returns the length of the object inside it,
# in this case course, which is 20
print(len(course))

# But the String also have methods that we can use
# For example, upper() returns the string in uppercase
print(course.upper())

# Note that to use a method from the String course, we
# have to use the . operator to access it
print(course.lower()) # This will print the string in lowercase

# There are many methods from the Strings, you can search them in the python documentation